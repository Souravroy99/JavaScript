/*
    ** Promise is an object that represents the eventual completion (resolved) or failure (rejected) of an asynchronous operation and its resulting value.

    ** It acts as a placeholder for a value that will be available in the future.

    ** A Promise can be in one of three states:

        i.) Pending → Initial state, neither fulfilled nor rejected.

       ii.) Fulfilled (Resolved) → The operation completed successfully, and the promise has a value.

      iii.) Rejected → The operation failed, and the promise has a reason (error).

*/